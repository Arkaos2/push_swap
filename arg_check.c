int is_digit(char *argv)
{
	int i;

	i = 0;
	while(argv[i])
	{
		if (!(argv[i] >= '0' && argv[i] <= '9'))
		{
			ft_putstr_fd("Error\n", 2);
			exit(1);
		}
		i++;
	}
	return (0);
}
